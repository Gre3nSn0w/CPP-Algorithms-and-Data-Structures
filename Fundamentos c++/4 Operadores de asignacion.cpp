#include <iostream>
using namespace std;

int main()
{
	//operadores de asignación
	
	int x=10;
	
	x ++;
	cout<<"El valor de x con incremeneto (++) es:"<<x<<endl;
	
	x --;
	cout<<"El valor de x con decremento (--) es:"<<x<<endl;
	
	x=x+1;
	cout<<"El valor de x con asignacion simple (=) es:"<<x<<endl;
	
	x -= 2;
	cout<<"El valor de x con resta y asignacion (-=) es:"<<x<<endl;
	
	x+=3;
	cout<<"El valor de x con suma y asignacion (+=) es:"<<x<<endl<<endl;
	
	
	system("pause");
	return 0;
}

